<?php

session_start();
include('connection.php');

if(!isset($_SESSION['logged_in'])){
  header('location: login.php');
  exit;
}

if(isset($_GET['logout'])){
  if(isset($_SESSION['logged_in'])){
    unset($_SESSION['logged_in']);
    unset($_SESSION['cli_email']);
    unset($_SESSION['cli_nome']);
    header('location: login.php');
    exit;
  }
}

if(isset($_POST['change_password'])){
  $senha = $_POST['senha'];
  $confirmPassword = $_POST['confirmPassword'];
  $cli_email = $_SESSION['cli_email'];

  //if passwords dont match
  if($senha !== $confirmPassword){
    header('location: change-password.php?error=As senhas não são iguais');
  }

  //if password is less than 8 char
  else if(strlen($senha) < 8){
    header('location: change-password.php?error=A senha deve ter ao menos 8 caracteres');

    //sem erros
  }else{

    $stmt = $conn->prepare("UPDATE cliente SET cli_senha=? WHERE cli_email=?");
    $stmt->bind_param('ss',md5($senha),$cli_email);

    if($stmt->execute()){
      header('location: change-password?message=Senha alterada com sucesso');
    }else{
      header('location: change-password.php?error=Não foi possível alterar a senha');
    }
  }
}





